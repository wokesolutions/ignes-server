package com.wokesolutions.ignes.util;

public class Boundaries {
	public double maxlat;
	public double minlat;
	public double maxlng;
	public double minlng;
	
	public Boundaries(double maxlat, double minlat, double maxlng, double minlng) {
		this.maxlat = maxlat;
		this.minlat = minlat;
		this.maxlng = maxlng;
		this.minlng = minlng;
	}
}